app.use('/', viewRouter);
app.use('/api/v1/tours', tourRouter);
app.use('/api/v1/users', userRouter);
app.use('/api/v1/reviews', reviewRouter);
app.use('/api/v1/bookings', bookingRouter);

/* Esto funciona por la pila de los middleware. si la ruta es correcta va a ingresar en los
app.use de arriba y este no se va a ejecutar */
app.all('*', (req, res, next) => {
  // get.all -> post,delete,patch..  * -> all routes
  // res.status(404).json({
  //   status: 'fail',
  //   message: `Cant find ${req.originalUrl} on this server!`,
  // });

  // const err = Error(`Cant find ${req.originalUrl} on this server!`);
  // err.status = 'fail';
  // err.statusCode = 404;

  //al pasarle un argumento en next automaticamente se brincan
  //todos los middlewares siguentes y se va directo al el de errorhandling
  next(new AppError(`Cant find ${req.originalUrl} on this server!`, 404));
});

app.use(globalErrorHandler);

module.exports = app;
